package com.ozguryazilim.raf.models;

import java.io.Serializable;
import java.util.Date;
import java.util.Objects;

/**
 * Bir belge imzalÄ± ise imza detay bilgileri
 * 
 * @author Hakan Uygun
 */
public class RafSignature implements Serializable{
   
    private String id;
    private String name;
    private String path;
    private Date created;
    private String createdBy;
    private String type;
    private String format;
    private String status;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public Date getCreated() {
        return created;
    }

    public void setCreated(Date created) {
        this.created = created;
    }

    public String getCreatedBy() {
        return createdBy;
    }

    public void setCreatedBy(String createdBy) {
        this.createdBy = createdBy;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getFormat() {
        return format;
    }

    public void setFormat(String format) {
        this.format = format;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    @Override
    public int hashCode() {
        int hash = 5;
        hash = 23 * hash + Objects.hashCode(this.id);
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final RafSignature other = (RafSignature) obj;
        if (!Objects.equals(this.id, other.id)) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "RafSignature{" + "id=" + id + ", name=" + name + ", path=" + path + ", created=" + created + ", createdBy=" + createdBy + ", type=" + type + ", format=" + format + ", status=" + status + '}';
    }
    
    
}
